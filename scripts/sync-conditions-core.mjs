// sync-conditions-core.mjs
//
// Vendors the HEADLESS slice of `src/shared/conditions-core` into the headless
// submodules that cannot import the app's `src/shared` directly.
//
// WHY this exists (the submodule import wall)
// -------------------------------------------
// `packages/codex-headless` and `packages/claude-code-headless` are independent
// git submodules with their OWN tsconfig, their OWN package boundary, and no
// path mapping back into the host app. They physically cannot `import` from
// `../../src/shared/conditions-core` — that path is outside their rootDir and
// belongs to a different package. So the contract + evaluator that MUST stay
// identical between app and headless are copied in, and this script is the
// single source of that copy. Edit `src/shared/conditions-core/*`, then run this
// to re-emit the vendored copies. The generated files carry a header banner so a
// future reader knows not to hand-edit them.
//
// WHY only contract.ts + evaluator.ts (the "headless slice")
// ----------------------------------------------------------
// conditions-core has two halves. The HEADLESS half (contract.ts: the wire
// types + module contract; evaluator.ts: the detect/dedupe engine) is pure
// TypeScript with zero runtime deps and zero React — it runs inside the headless
// process. The RENDERER half (view.ts, ConditionOutlet.tsx, dispatch.ts) pulls
// in React and app-only concerns and has NO business inside a headless package.
// We vendor ONLY the headless slice. If you add a new headless-side file to
// conditions-core, add it to HEADLESS_SLICE below.
//
// WHY a banner instead of a generated/ folder convention
// ------------------------------------------------------
// The vendored copies live under each package's `src/conditions/core/` so the
// migrated modules import them with a short relative path (`./core/contract.js`).
// They are real, committed, typechecked source inside the submodule (the
// submodule builds standalone in CI without the host app present), so they can't
// be gitignored. The per-file banner is the guardrail against someone editing
// the copy instead of the source.

import { readFile, writeFile, mkdir } from 'node:fs/promises'
import path from 'node:path'
import { fileURLToPath } from 'node:url'

const __dirname = path.dirname(fileURLToPath(import.meta.url))
// scripts/ sits at the repo root; conditions-core is under src/shared.
const REPO_ROOT = path.resolve(__dirname, '..')
const SOURCE_DIR = path.join(REPO_ROOT, 'src', 'shared', 'conditions-core')

// The exact files that make up the headless slice. Order is irrelevant (each is
// copied independently), but evaluator.ts imports contract.ts via `./contract.js`
// so both MUST be present together for the vendored copy to typecheck.
const HEADLESS_SLICE = ['contract.ts', 'evaluator.ts']

// Banner stamped at the top of every generated file. Kept byte-stable so a
// re-run produces a no-op diff when the source is unchanged.
const BANNER =
  '// GENERATED by scripts/sync-conditions-core.mjs — edit src/shared/conditions-core, do not edit here\n'

// Vendor targets. Each entry is a package whose headless conditions slice gets
// the copy. `sync: false` wires the path for a package WITHOUT copying yet — the
// claude-code-headless migration is a later PR, so its destination is recorded
// here (so the next session reuses this exact script) but skipped in this run.
const TARGETS = [
  {
    name: 'codex-headless',
    dest: path.join(
      REPO_ROOT,
      'packages',
      'codex-headless',
      'src',
      'conditions',
      'core',
    ),
    sync: true,
  },
  {
    name: 'claude-code-headless',
    // PR-3 flipped this to `sync: true`. The claude-code-headless conditions
    // migration now exists (src/conditions/{trustDialog,permissionPrompt,
    // resumePrompt,compaction}.ts + modules.ts + index.ts), and those modules
    // import the vendored core via `./core/contract.js` / `./core/evaluator.js`.
    // The destination path was already declared by PR-2 so this flip is the
    // intended one-line change — the script body is otherwise untouched. The
    // generated copies are byte-identical to codex-headless's copies (same
    // source, same banner); that cross-package identity is the entire reason
    // the core is vendored through ONE script rather than hand-copied per package.
    dest: path.join(
      REPO_ROOT,
      'packages',
      'claude-code-headless',
      'src',
      'conditions',
      'core',
    ),
    sync: true,
  },
]

async function syncTarget(target) {
  await mkdir(target.dest, { recursive: true })
  for (const file of HEADLESS_SLICE) {
    const src = path.join(SOURCE_DIR, file)
    const raw = await readFile(src, 'utf8')
    // Prepend the banner. The source file's own header comment follows it, so the
    // generated file reads: banner, blank line, original WHY comment, code.
    const stamped = `${BANNER}\n${raw}`
    const out = path.join(target.dest, file)
    await writeFile(out, stamped)
    console.error(`  wrote ${path.relative(REPO_ROOT, out)}`)
  }
}

async function main() {
  for (const target of TARGETS) {
    if (!target.sync) {
      console.error(`skip ${target.name} (sync: false — wired for a later PR)`)
      continue
    }
    console.error(`sync ${target.name}:`)
    await syncTarget(target)
  }
  console.error('done.')
}

main().catch((err) => {
  console.error(err)
  process.exit(1)
})
